def case(case_id, domain, summary_en, summary_tr, failure, taxonomy, risk_axis,
         safe_en, safe_tr, seed_tr, pair_id, drift_dim, setting,
         sourcecheckup=False, track_a=None, track_b=None):
    """Build one case dict. language/seed only set for TR rows."""
    is_tr = case_id.startswith("DRIFT-TR")
    row = {
        "case_id": case_id,
        "clinical_domain": domain,
        "setting": setting,
        "synthetic_case_summary": summary_tr if is_tr else summary_en,
        "failure_pattern": failure,
        "taxonomy_pattern_ids": taxonomy,
        "risk_axis": risk_axis,
        "sourcecheckup_needed": sourcecheckup,
        "clinician_review_needed": True,
        "safe_answer_expectation": safe_tr if is_tr else safe_en,
        "release_gate": "synthetic_preview_only",
        "track_a_relevance": track_a or "Bilingual safety drift protocol case for cross-language clinical urgency preservation.",
        "track_b_relevance": track_b or "Failure Atlas pattern for Turkish-English safety boundary drift measurement.",
        "synthetic_only": True,
        "patient_data_used": False,
        "clinical_use_allowed": False,
        "drift_pair_id": pair_id,
        "drift_dimension": drift_dim,
    }
    if is_tr:
        row["language"] = "tr"
        row["turkish_prompt_seed"] = seed_tr
    return row


